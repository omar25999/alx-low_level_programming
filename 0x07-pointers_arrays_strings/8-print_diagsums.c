#include "main.h"
/**
 * print_diagsums - function that prints the sum of the two diagonals
 * @a: pointer to arrays
 * @size: size of array
 */

void print_diagsums(int *a, int size)
{
	int x;
	int y;

	for (x = 0; x < 0; x++)
	{
		for (y = 0; y < 0; y++)
		{
			_putchar(a[x][y]);
			_putchar('\n');
		}
	}
}

